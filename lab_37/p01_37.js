// 1. Create an empty array called fruits
let fruits = [];

// 2. Add "apple", "banana", and "cherry" to the array using push()
fruits.push("apple", "banana", "cherry");

// 3. Remove the last element using pop(), store it in a variable, and log it
let removedFruit = fruits.pop();
console.log("Removed element:", removedFruit); // This will log "cherry"

// 4. Print the final array
console.log("Final array:", fruits); // This will log ["apple", "banana"]